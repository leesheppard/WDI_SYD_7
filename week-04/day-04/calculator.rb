class Calculator
  def self.add(number_one, number_two)
    number_one + number_two
  end

  def self.subtract(number_one, number_two)
    number_one - number_two
  end

  def self.multiply(number_one, number_two)
    number_one * number_two
  end

  def self.divide(number_one, number_two)
    number_one / number_two
  end
end